# https://www.acmicpc.net/problem/1193
# 수학 / 구현
# [1/1],
# [1/2, 2/1],
# [3/1, 2/2, 1/3],
# [1/4, 2/3, 3/3, 4/1]
# 이런식으로 각 대각선의 요소의 갯수가 규칙이 있다.  1번은 1개 대각선 2번은 2개 , 3번은 3개.. n번은 n개씩 늘어난다.
# 대각선 번호가 홀수면, 분자는 n부터 1까지 줄어들고, 분모는 1부터 n까지 늘어난다. (짝수는 그 반대로 진행)
# 이렇게 보면, 대각선 번호만 알아내면, 해당 N번째 수가 분수가 무엇인지 찾아낼 수 있다.

N = int(input())
요소갯수, line = 0, 0
while N > 요소갯수:
    line += 1
    요소갯수 += line
    print(요소갯수)
print('result:', 요소갯수, line)

분자, 분모 = 0, 0
# 반복문이 필요없다.
if line % 2 != 0:
    분자 = (요소갯수 - N) + 1
    분모 = line - (요소갯수 - N)
else:
    분자 = line - (요소갯수 - N)
    분모 = (요소갯수 - N) + 1

print(f'{분자}/{분모}')
